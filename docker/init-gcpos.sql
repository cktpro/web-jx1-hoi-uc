CREATE TABLE IF NOT EXISTS gc_log (
    id INT AUTO_INCREMENT PRIMARY KEY,
    user VARCHAR(100),
    seri VARCHAR(200),
    pin VARCHAR(200),
    type VARCHAR(50),
    menhgia INT DEFAULT 0,
    status TINYINT DEFAULT 0,
    gia TINYINT DEFAULT 0,
    createTime DATETIME DEFAULT CURRENT_TIMESTAMP,
    date DATE DEFAULT (CURRENT_DATE)
) ENGINE=InnoDB;

CREATE TABLE IF NOT EXISTS gc_logxu (
    id INT AUTO_INCREMENT PRIMARY KEY,
    user VARCHAR(100),
    xutru INT DEFAULT 0,
    createTime DATETIME DEFAULT CURRENT_TIMESTAMP
) ENGINE=InnoDB;

CREATE TABLE IF NOT EXISTS gc_logcongxu (
    id INT AUTO_INCREMENT PRIMARY KEY,
    admin VARCHAR(100),
    user VARCHAR(100),
    xucong INT DEFAULT 0,
    createTime DATETIME DEFAULT CURRENT_TIMESTAMP
) ENGINE=InnoDB;

CREATE TABLE IF NOT EXISTS gc_shop (
    id INT AUTO_INCREMENT PRIMARY KEY,
    name VARCHAR(255),
    xu INT DEFAULT 0,
    data VARCHAR(255) DEFAULT ''
) ENGINE=InnoDB;

CREATE TABLE IF NOT EXISTS gc_server (
    id INT AUTO_INCREMENT PRIMARY KEY,
    sid INT,
    name VARCHAR(100),
    db_name VARCHAR(100)
) ENGINE=InnoDB;

CREATE TABLE IF NOT EXISTS KTCoins (
    id INT AUTO_INCREMENT PRIMARY KEY,
    UserName VARCHAR(100),
    KCoin INT DEFAULT 0,
    UpdateTime DATETIME DEFAULT CURRENT_TIMESTAMP
) ENGINE=InnoDB;
