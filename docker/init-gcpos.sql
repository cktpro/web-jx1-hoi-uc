CREATE DATABASE IF NOT EXISTS gcpos CHARACTER SET utf8mb4 COLLATE utf8mb4_unicode_ci;
USE gcpos;

CREATE TABLE IF NOT EXISTS LoginTables (
    ID INT AUTO_INCREMENT PRIMARY KEY,
    LoginName VARCHAR(100) NOT NULL UNIQUE,
    Password VARCHAR(100) NOT NULL,
    Email VARCHAR(200) DEFAULT '',
    CreateTime DATETIME DEFAULT CURRENT_TIMESTAMP
) ENGINE=InnoDB;

CREATE TABLE IF NOT EXISTS gc_user (
    id INT AUTO_INCREMENT PRIMARY KEY,
    user VARCHAR(100) NOT NULL UNIQUE,
    pass VARCHAR(100) NOT NULL,
    xu INT DEFAULT 0,
    gem INT DEFAULT 0,
    email VARCHAR(200) DEFAULT '',
    createTime DATETIME DEFAULT CURRENT_TIMESTAMP
) ENGINE=InnoDB;

CREATE TABLE IF NOT EXISTS gc_info (
    id INT AUTO_INCREMENT PRIMARY KEY,
    title VARCHAR(255) DEFAULT 'JX1 Game',
    urlgame TEXT,
    page VARCHAR(255) DEFAULT '',
    code TEXT,
    email VARCHAR(200) DEFAULT '',
    keyapi VARCHAR(255) DEFAULT '',
    khuyenmai TEXT,
    atmbank TEXT,
    momo TEXT
) ENGINE=InnoDB;

